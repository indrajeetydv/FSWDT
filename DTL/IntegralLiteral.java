public class IntegralLiteral{
	public static void main(String[] args){
		int decimalValue=123;//0-9  
		int octalValue=012345;//0
		int hexaDecimalvalue=0x12af;//0X
		int binaryValue=0b1010;//0b 0B

		System.out.println("Decimal Value: "+decimalValue);
		System.out.println("Ocatal Value: "+octalValue);
		System.out.println("HexaDecimal  Value: "+hexaDecimalvalue);
		System.out.println("Binary Value: "+binaryValue);

	}
}