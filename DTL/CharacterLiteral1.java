public class CharacterLiteral1{
	public static void main(String[] args){
		char ch='0';
		System.out.println(ch);//0

		char ch1a='a';
		System.out.println(ch1a);//a

		char ch1A='A';
		System.out.println(ch1A);//A

		char ch11=48;
		System.out.println(ch11);//0		

		int i='a';
		System.out.println(i);//97
	}
}